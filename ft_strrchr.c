#include "libft.h"

char		*ft_strrchr(char *s, int c)
{
	unsigned long	i;

	i = ft_strlen(s);
	while (i)
	{
		if (s[--i] == (char)c)
			return (&s[i]);
	}
	return (NULL);
}
