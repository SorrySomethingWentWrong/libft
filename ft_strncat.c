#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	char	*ptrs1;

	ptrs1 = &s1[ft_strlen(s1)];
	while (*s2 && --n)
		*ptrs1++ = *s2++;
	*ptrs1 = *s2;
	return (s1);
}
